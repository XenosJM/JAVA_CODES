package edu.java.for01;

public class ForMain01 {

	public static void main(String[] args) {
		System.out.println("for 반복문");
		
		// 1 ~ 10까지 출력
		for (int x = 1; x <= 10; x++) {
			System.out.println(x);
		}
		
		System.out.println("===============");
		// 1, 3, 5, 7, 9 를 출력
		for (int x = 1; x <= 9; x++) { // x += 2도 가능
			if (x % 2 == 1) {
				System.out.println(x);
			}
		}
		
		System.out.println("===============");
		// 15, 14, 13, 12, ... 1을 출력
		for (int x = 15; x >= 1; x--) {
			System.out.println(x);
		}
		
		
	} // end main()

} // end ForMain01
